"""
Note- 1. In this program, we have used built-in functions bin(), oct() and
         hex() to convert the given decimal number into respective number
         systems.

      2. These functions take an integer (in decimal) and return a string.

      3. A number with the prefix 0b is considered binary, 0o is considered
         octal and 0x as hexadecimal.
"""

print(bin(344))
print(oct(344))
print(hex(344))
