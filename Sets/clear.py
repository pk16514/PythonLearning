"""
   1. The syntax of clear() method is: set.clear()

   2. clear() method doesn't take any parameters.

   3. clear() method doesn't return any value and returns a None.
"""
# Example-1


vowels = {'a', 'e', 'i', 'o', 'u'}
print('Vowels (before clear):', vowels)

vowels.clear()
print('Vowels (after clear):', vowels)
